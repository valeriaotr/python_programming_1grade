    for file_name in ["puzzle1.txt", "puzzle2.txt", "puzzle3.txt"]:
        answer = read_sudoku(file_name)
        sol = solve(answer)
        if not sol:
            print(f"Puzzle {file_name} can't be solved")
        else:
            display(sol)